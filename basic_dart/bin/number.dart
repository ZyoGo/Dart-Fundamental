void main() {
  // int digunakan untuk bilangan bulat
  int age = 21;
  // double digunakan untuk bilangan decimal menggunakan format internasional (titik .)
  double number2 = 9.99;
  print(age);
  print(number2);

  // jika kita ingin menggunakan tipe data number yang bisa int ataupun double
  num number3 = 10;
  print(number3);
  number3 = 12.12;
  print(number3);
}
